// Operadores: Asignación, Comparación y Aritméticos.

// Operadores Binarios - se utiliza en dos valores.
3 + 2;
50 - 10;
10 * 20;
20 / 2;
10 % 2; 

// Operador de Asignación.
var a = "valor";

// Operadores de Comparación. 
3 == "3" // compara solo el valor.
3 === "3" // compara el valor y el tipo de dato.
5 < 3;
5 > 3;
5 <= 6;
5 >= 6;

// Operadores lógicos.
a && b; // And
a || b; // Or
!false; // aporador Not.
// Operador Unitario - se utiliza en un valor.

// Incrementadores
var edad = 40

edad++; // Incrementa en 1
++edad; // Incrementa en 1

edad--; // Decrementa en 1
--edad; // Decrementa en 1

// otros EJ
edad += 2;
edad -= 2;
edad *= 2;
edad /= 2;
